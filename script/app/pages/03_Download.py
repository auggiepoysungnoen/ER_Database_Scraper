def _format_size(gb) -> str:
    if gb is None or (isinstance(gb, float) and pd.isna(gb)):
        return "Unknown"
    try:
        gb = float(gb)
        if gb >= 1:
            return f"{gb:.1f} GB"
        return f"{gb*1024:.0f} MB"
    except (ValueError, TypeError):
        return str(gb)


def _generate_shell_script(selected: pd.DataFrame) -> str:
    lines = [
        "#!/usr/bin/env bash",
        "# Endometrial Receptivity Database — Download Script",
        "# Generated by Hickey Lab | Duke University",
        "# Modify OUTPUT_DIR to change download destination",
        "",
        'OUTPUT_DIR="${1:-./downloads}"',
        "mkdir -p \"$OUTPUT_DIR\"",
        "",
    ]
    for modality, grp in selected.groupby("modality"):
        lines.append(f"# ── {modality} ──────────────────────────────────────────")
        for _, row in grp.iterrows():
            acc = row["accession"]
            url = row["download_url"]
            md5 = row.get("md5", "")
            if row.get("controlled_access"):
                lines.append(f"# WARNING: {acc} is controlled-access — manual dbGaP/EGA request required")
                lines.append(f"# URL: {url or 'N/A'}")
            else:
                if md5:
                    lines.append(f"# MD5: {md5}")
                if url:
                    lines.append(f'wget -c -P "$OUTPUT_DIR" "{url}"  # {acc}')
                else:
                    lines.append(f"# {acc}: no direct download URL available")
        lines.append("")
    lines.append("echo 'Download complete.'")
    return "\n".join(lines)


def _generate_python_script(selected: pd.DataFrame) -> str:
    lines = [
        '"""Endometrial Receptivity Database — Python Download Script',
        "Generated by Hickey Lab | Duke University",
        '"""',
        "",
        "import os",
        "import subprocess",
        "import sys",
        "",
        'OUTPUT_DIR = sys.argv[1] if len(sys.argv) > 1 else "./downloads"',
        "os.makedirs(OUTPUT_DIR, exist_ok=True)",
        "",
        "datasets = [",
    ]

    for _, row in selected.iterrows():
        acc = row["accession"]
        url = row["download_url"]
        controlled = row.get("controlled_access", False)
        md5 = row.get("md5", "")
        lines.append(
            f'    {{"accession": {acc!r}, "url": {url!r}, '
            f'"controlled": {controlled!r}, "md5": {md5!r}}},'
        )

    lines += [
        "]",
        "",
        "for ds in datasets:",
        '    acc = ds["accession"]',
        '    url = ds["url"]',
        '    if ds["controlled"]:',
        '        print(f"SKIP {acc}: controlled access — request via dbGaP/EGA")',
        "        continue",
        '    if not url:',
        '        print(f"SKIP {acc}: no download URL")',
        "        continue",
        '    dest = os.path.join(OUTPUT_DIR, os.path.basename(url))',
        '    print(f"Downloading {acc} -> {dest}")',
        '    subprocess.run(["wget", "-c", "-O", dest, url], check=True)',
        '    if ds["md5"]:',
        '        print(f"  MD5: {ds[\'md5\']}")',
        "",
        "print('Done.')",
    ]
    return "\n".join(lines)


