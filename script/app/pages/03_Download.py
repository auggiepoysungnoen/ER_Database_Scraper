# Compute total size
def _parse_gb(size_str: str) -> float:
    if not size_str or size_str == "Unknown":
        return 0.0
    try:
        if "GB" in size_str:
            return float(size_str.replace("GB", "").strip())
        if "MB" in size_str:
            return float(size_str.replace("MB", "").strip()) / 1024
    except ValueError:
        pass
    return 0.0

